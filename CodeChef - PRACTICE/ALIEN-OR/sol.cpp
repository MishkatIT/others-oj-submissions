int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int tc;
    cin >> tc;
    while (tc--) {
        int n, k;
        cin >> n >> k;
        vector<bool> ok (k + 5);
        for (int i = 0; i < n; i++) {
            string x;
            cin >> x;
            reverse(x.begin(), x.end());
            if (count(x.begin(), x.end(), '1') == 1) {
                for (int j = 0; j < k; j++) {
                    if (x[j] == '1') {
                        ok[j + 1] = true;
                        break;
                    }
                }
            }
        }
        bool okkk = true;
        for (int i = 1; i <= k; i++) {
            okkk &= ok[i];
        }
        cout << (okkk ? "YES" : "NO") << '\n';
    }
    return 0;
}